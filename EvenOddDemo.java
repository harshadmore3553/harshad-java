public class EvenOddDemo {
    public static void main(String[] args) {
        int i;
        for(i=1;i<=100;i++)
        {
            if(i%2==0)
                System.out.println("No. is even\t"+ i);
            else
                System.out.println("NO. is odd\t"+ i);
        }

    }
    
}
