public class TypeCasting {
    public static void main(String[] args) {
        // implicit type casting (int to double)
        int a,b;
        a=11;
        b=20;
        double a1,b1,div;
        a1=a;
        b1=b;
        div=a1/b1;
        System.out.println("div is"+div);
        //explicit type casting (double to integer)
        //destination variable=(destination data type)source variable
        double pi=3.14;
        inta2=(int)pi;
        System.out.println("b value is"+inta2);

    }
}
